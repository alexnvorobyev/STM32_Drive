#ifndef MISRA_TYPES_H_
#define MISRA_TYPES_H_

typedef char 			int8_t;	
typedef short 			int16_t;
typedef int 			int32_t;
typedef unsigned char 	uint8_t;
typedef unsigned short 	uint16_t;
typedef unsigned int 	uint32_t;

typedef uint8_t boolean;

#endif /* MISRA_TYPES_H_ */
